const zoomImage = document.getElementById('zoomImage1');

zoomImage1.addEventListener('click', function() {
  if (zoomImage1.style.transform === 'scale(1.5)') {
    zoomImage1.style.transform = 'scale(1)';
    zoomImage1.style.cursor = 'zoom-in';
  } else {
    zoomImage1.style.transform = 'scale(1.5)';
    zoomImage1.style.cursor = 'zoom-out';
  }
})
